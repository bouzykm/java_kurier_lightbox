package com.lbapp.MasterKurier.Trasa;

public class TrasaObject {
    String trasa, liczbaToreb, liczbaBoxow, liczbaBoxowOstTor;
    int id;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getTrasa() {
        return trasa;
    }

    public void setTrasa(String trasa) {
        this.trasa = trasa;
    }

    public String getLiczbaToreb() {
        return liczbaToreb;
    }

    public void setLiczbaToreb(String liczbaToreb) {
        this.liczbaToreb = liczbaToreb;
    }

    public String getLiczbaBoxow() {
        return liczbaBoxow;
    }

    public void setLiczbaBoxow(String liczbaBoxow) {
        this.liczbaBoxow = liczbaBoxow;
    }

    public String getLiczbaBoxowOstTor() {
        return liczbaBoxowOstTor;
    }

    public void setLiczbaBoxowOstTor(String liczbaBoxowOstTor) {
        this.liczbaBoxowOstTor = liczbaBoxowOstTor;
    }
}
